# 复杂的堆栈不平衡示例 - 用于测试静态分析脚本
# 包含多个函数、中断处理程序、条件分支、动态分配等场景

.globl func1
.globl func2
.globl func3
.globl isr_14          # 缺页异常处理程序（有错误码）
.globl isr_8           # 双重故障（有错误码）
.globl isr_7           # 设备不可用（无错误码）
.globl nested_calls

.section .text

# ------------------------------------------------------------
# func1: 简单的寄存器保存遗漏（忘记 pop rbx）
# 预期：脚本在 ret 前应检测到偏移 -8
# ------------------------------------------------------------
func1:
    push    %rbp
    mov     %rsp, %rbp
    push    %rbx        # 保存 rbx，偏移 -16
    push    %r12        # 保存 r12，偏移 -24
    # ... 一些操作 ...
    pop     %r12        # 恢复 r12，偏移 -16
    # 忘记 pop %rbx
    pop     %rbp        # 恢复 rbp，偏移 -8
    ret                 # 不平衡：实际偏移 -8，应为 0

# ------------------------------------------------------------
# func2: 条件分支中的不平衡
# 根据参数（假设在 rdi）决定分支，一条分支多压了一个值未弹出
# 预期：脚本应报告两条路径，其中一条 ret 前偏移为 -8，另一条为 0
# ------------------------------------------------------------
func2:
    push    %rbp
    mov     %rsp, %rbp
    cmp     $0, %rdi
    je      .Lbranch
    # 分支1：压入额外值，但忘记弹出
    push    $0x1234     # 压入立即数，偏移 -8（相对于初始）
    # ... 一些代码 ...
    jmp     .Lret
.Lbranch:
    # 分支2：正常路径，没有额外压栈
    nop
.Lret:
    pop     %rbp        # 从 rbp 恢复，但分支1 中 push 的值还在栈上！
    ret                 # 分支1 ret 前偏移 -8，分支2 偏移 0

# ------------------------------------------------------------
# func3: 使用 alloca 动态分配堆栈，但忘记释放
# 注意：静态分析无法确定 alloca 分配的大小（取决于 rdi）
# 但脚本可能通过 add/sub 指令跟踪，这里我们用 sub 模拟 alloca
# 实际中 alloca 通常用 sub rsp, rdi 实现，然后通过 mov rsp, rbp 恢复
# 但这里故意不用 leave，导致不平衡
# ------------------------------------------------------------
func3:
    push    %rbp
    mov     %rsp, %rbp
    # 模拟 alloca：根据 rdi 分配可变大小堆栈空间
    sub     %rdi, %rsp   # 分配 rdi 字节，偏移量 -rdi（未知）
    # 使用栈空间 ...
    # 没有恢复 rsp 就返回
    pop     %rbp        # pop rbp，但 rsp 没有恢复，此时 rsp 仍指向分配的缓冲区
    ret                 # 严重不平衡：rsp 未恢复到原始位置

# ------------------------------------------------------------
# isr_14: 缺页异常处理程序，CPU 自动压入了 RIP+CS+RFLAGS+错误码（共32字节）
# 我们在处理中压入一些寄存器，但忘记弹出，导致 iretq 前不平衡
# 预期：脚本应为 isr_14 设置初始偏移 -32，检查 iretq 前是否为 0
# ------------------------------------------------------------
isr_14:
    push    %rax        # 压入 rax，偏移 -40（相对于初始 rsp）
    push    %rcx        # 压入 rcx，偏移 -48
    # 处理缺页...
    pop     %rcx        # 恢复 rcx，偏移 -40
    # 忘记 pop rax
    iretq               # 不平衡：偏移 -40，应为 0

# ------------------------------------------------------------
# isr_8: 双重故障（有错误码），正确平衡
# 预期：检查通过
# ------------------------------------------------------------
isr_8:
    push    %rax
    push    %rcx
    # 处理...
    pop     %rcx
    pop     %rax
    iretq               # 平衡：偏移 0（初始 -32，push/pop 各 +/-8，最终归零）

# ------------------------------------------------------------
# isr_7: 设备不可用（无错误码），但我们在 iretq 前多 pop 了一次
# 导致堆栈指针过高，也可能不平衡
# 预期：初始偏移 -24，多 pop 一次导致偏移 -16，iretq 前偏移应为 -16（实际弹出 24 字节后为 +8，但 iretq 期望 0）
# ------------------------------------------------------------
isr_7:
    push    %rax        # -32
    # 处理...
    pop     %rax        # -24
    pop     %rbx        # 多 pop 了一个未压入的寄存器！导致 -16
    iretq               # iretq 弹出 24 字节后，rsp 应为 -16+24=+8，不平衡

# ------------------------------------------------------------
# nested_calls: 展示调用其他函数，被调用者可能破坏堆栈
# 假设 callee 使用 cdecl（调用者清理），但脚本如果不知道 callee 的约定，
# 可能会认为 callee 不影响堆栈，导致误判。
# 这里故意让 callee 使用 stdcall（被调用者清理），而调用者没有调整，从而造成不平衡。
# 脚本无法分析 callee 内部，因此可能漏报。
# ------------------------------------------------------------
nested_calls:
    push    %rbp
    mov     %rsp, %rbp
    call    callee_stdcall   # 此函数用 stdcall，会自己清理参数，但这里我们没有压参数？实际上我们压了两个参数
    # 在 stdcall 中，被调用者会执行 ret 8 来清理两个参数（每个 8 字节）
    # 调用者不应该再添加任何清理代码
    # 我们故意压入两个参数（模拟 cdecl 的调用方式，但 callee 是 stdcall）
    push    $0x1111
    push    $0x2222
    call    callee_stdcall   # 这次 callee 会清理参数，但调用者没有压参数？实际上前面压了
    # 但是调用者按照 cdecl 习惯，可能会在 call 后 add $16, %rsp，导致重复清理
    add     $16, %rsp        # 错误：重复清理，导致堆栈指针过高
    pop     %rbp
    ret

callee_stdcall:
    # 这是一个 stdcall 函数，期望两个参数，自己清理堆栈
    push    %rbp
    mov     %rsp, %rbp
    # 使用参数 ...
    pop     %rbp
    ret     $16               # 清理 16 字节的参数（两个 8 字节）
    